 <span class="level5 pli-lang">Suṇātu me ayye saṅgho.
Ajjuposatho pannaraso,
yadi saṅghassa pattakallaṁ, saṅgho uposathaṁ kareyya, pātimokkhaṁ uddiseyya.</span>
